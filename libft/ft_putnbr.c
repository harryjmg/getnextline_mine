#include "libft.h"

void	ft_putnbr(int n)
{
	int unit;

	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n < 10)
	{
		ft_putchar(n + '0');
		return ;
	}
	unit = n % 10;
	ft_putnbr(n / 10);
	ft_putchar(unit + '0');
}
